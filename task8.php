<?
function sum($a, $b) {
	return $a + $b;
}

function subtraction($a, $b) {
	return $a - $b;
}

function multiply($a, $b) {
	return $a * $b;
}

function divide($a, $b) {
	return $a / $b;
}
echo "Сумма чисел: ";
echo sum(4, 2);
echo "<br>";
echo "Разность чисел: ";
echo subtraction(4, 2);
echo "<br>";
echo "Произведение чисел: ";
echo multiply(4, 2);
echo "<br>";
echo "Деление чисел: ";
echo divide(4, 2);
echo "<br>";
?>